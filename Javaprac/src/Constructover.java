
public class Constructover {
	
	
	
	Constructover(){
		System.out.println("I am a Constructor without any parameters");
	}
	
	Constructover(int a ){
		System.out.println("This is a constructor with int value and value is "+a);
		
	
	}
	
	Constructover(double a ){
		System.out.println("This is a constructor with double value and value is "+a);
		
	
	}
	
	Constructover(double a, int b ){
		System.out.println("This is a constructor with double and integer value and value is "+a +"and"+" "+b);
		
	
	}
	
	
	void simpleMethod() {
		System.out.println("simple()");
	}

}
