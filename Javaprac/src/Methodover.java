
public class Methodover {
	
	void method1() {
		System.out.println("method1()");
	}
	
	void method1(int a) {
		System.out.println(a);
		
	}
	
	public void method1(double b) {
		System.out.println(b);
	}
	
	public static void method1(int a, double b) {
		System.out.println(a);
		System.out.println(b);
	}

	public static void method1(double a, int b) {
		System.out.println(a);
		System.out.println(b);
	}
	
}
